package projeto;

public class Funcoes {
    
    //método que soma 2 números
    public static int somarNumeros(int valor1, int valor2){
        int resultado = valor1 + valor2;
          
        return resultado;
    }
    
    public int subtrairNumeros(int valor1, int valor2){
        int resultado = valor1 - valor2;
        return resultado;
    }
    
}
